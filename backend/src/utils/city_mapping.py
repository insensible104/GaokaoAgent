def get_school_city(school_name: str) -> str:
    """
    获取学校所在城市

    Args:
        school_name: 学校名称

    Returns:
        城市名称，如果未找到返回"未知"

    注意事项：
        - 分校的城市映射需要手动添加到SCHOOL_CITY_MAP中
        - 例如："哈尔滨工业大学(深圳)": "深圳"
        - 例如："山东大学威海分校": "威海"
        - 如果分校未在数据库中，会fallback到主校区城市（可能不准确）
    """
    # 精确匹配
    if school_name in SCHOOL_CITY_MAP:
        return SCHOOL_CITY_MAP[school_name]

    # 修复问题B-6：改进模糊匹配，避免过宽匹配
    # 去除"大学"、"学院"后缀后再匹配
    school_base = school_name.replace("大学", "").replace("学院", "").replace("分校", "")
    for school, city in SCHOOL_CITY_MAP.items():
        school_key = school.replace("大学", "").replace("学院", "").replace("分校", "")
        # 使用完整名称匹配，而不是子串匹配
        if school_base == school_key and school_base:
            return city
        # 修复NEW-3：分校匹配逻辑（可能不准确，建议手动添加到数据库）
        # 如果是分校情况（例如：山东大学威海分校 vs 山东大学）
        # 这里会返回主校区城市（济南），但实际应该是威海
        # 建议在SCHOOL_CITY_MAP中显式添加："山东大学威海分校": "威海"
        if school_base and school_key and school_base.startswith(school_key):
            return city

    # 未找到，返回"未知"
    return "未知"


def calculate_city_preference_score(
    city: str,
    preferred_cities: list,
    excluded_cities: list
) -> float:
    """
    计算城市偏好评分

    Args:
        city: 学校所在城市
        preferred_cities: 偏好城市列表
        excluded_cities: 排除城市列表

    Returns:
        评分系数（0.5-1.5）
        - 1.0: 无偏好
        - 1.3: 偏好城市
        - 0.5: 排除城市
    """
    # 如果是排除城市，大幅降低评分
    if excluded_cities:
        for excluded in excluded_cities:
            if excluded in city or city in excluded:
                return 0.5  # 降低50%

    # 如果是偏好城市，提升评分
    if preferred_cities:
        for preferred in preferred_cities:
            if preferred in city or city in preferred:
                return 1.3  # 提升30%

    # 默认不调整
    return 1.0
